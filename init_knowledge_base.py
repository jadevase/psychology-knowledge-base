def load_index():
    """加载索引文件"""
    if os.path.exists(INDEX_FILE):
        with open(INDEX_FILE, 'r', encoding='utf-8') as f:
            return json.load(f)
    return {
        "created_at": datetime.now().strftime("%Y-%m-%d"),
        "total_lessons": 0,
        "current_day": 0,
        "stages": {},
        "lessons": []
    }


def save_index(index_data):
    """保存索引文件"""
    with open(INDEX_FILE, 'w', encoding='utf-8') as f:
        json.dump(index_data, f, ensure_ascii=False, indent=2)


def generate_daily_lesson(day_data):
    """生成每日学习内容"""
    day = day_data["day"]
    topic = day_data["topic"]
    stage = day_data["stage"]
    
    content = f"""# 第{day}天：{topic}

## 学习阶段
{stage}

## 学习目标
"""
    for obj in day_data["learning_objectives"]:
        content += f"- {obj}\n"
    
    content += f"""
## 核心概念
"""
    for concept in day_data["key_concepts"]:
        content += f"### {concept.split('：')[0]}\n"
        if '：' in concept:
            definition = concept.split('：', 1)[1]
            content += f"{definition}\n\n"
            # 扩展讲解
            content += f"**详细讲解**：\n"
            content += f"这个概念是心理学中的基础知识点。让我们深入理解：\n\n"
            content += f"**定义解析**：{definition}\n\n"
            content += f"**重要性**：这是理解后续心理学知识的基础，需要重点掌握。\n\n"
            content += f"**应用场景**：在实际生活和心理咨询工作中，这个概念帮助我们更好地理解人的心理活动和行为表现。\n\n"
            content += f"**记忆要点**：\n"
            content += f"- 关键词：{concept.split('：')[0]}\n"
            content += f"- 核心特征：{definition[:50]}...\n"
            content += f"- 相关联系：与本章其他概念相互关联，形成完整的知识体系\n\n"
            content += "---\n\n"
    
    content += """
## 学习时间分配建议
- **通读核心概念**：15 分钟
- **深入理解每个概念**：25 分钟
- **回顾与总结**：10 分钟

## 重点提示
⚠️ 本章节内容是心理学基础知识的核心，务必确保完全理解每个概念的定义和内涵。
💡 建议做笔记整理关键概念，便于后续复习。

---
*学习时间：约 50 分钟*
"""
    return content



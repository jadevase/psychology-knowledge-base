def main():
    print("开始初始化心理学知识库...")
    
    # 创建目录
    os.makedirs(DAILY_LESSONS_DIR, exist_ok=True)
    os.makedirs(PRACTICE_QUESTIONS_DIR, exist_ok=True)
    
    # 加载现有索引
    index_data = load_index()
    
    # 生成前 7 天的内容
    lessons_added = 0
    for day_data in LEARNING_PLAN:
        day = day_data["day"]
        
        # 检查是否已存在
        lesson_file = os.path.join(DAILY_LESSONS_DIR, f"day-{day:03d}.md")
        if os.path.exists(lesson_file):
            print(f"第{day}天内容已存在，跳过")
            continue
        
        # 生成学习内容
        lesson_content = generate_daily_lesson(day_data)
        with open(lesson_file, 'w', encoding='utf-8') as f:
            f.write(lesson_content)
        
        # 生成练习题
        questions_file = os.path.join(PRACTICE_QUESTIONS_DIR, f"day-{day:03d}-questions.md")
        questions_content = generate_practice_questions(day_data)
        with open(questions_file, 'w', encoding='utf-8') as f:
            f.write(questions_content)
        
        # 更新索引
        index_data["lessons"].append({
            "day": day,
            "topic": day_data["topic"],
            "stage": day_data["stage"],
            "lesson_file": f"daily-lessons/day-{day:03d}.md",
            "questions_file": f"practice-questions/day-{day:03d}-questions.md",
            "created_at": datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        })
        
        # 更新阶段信息
        stage = day_data["stage"]
        if stage not in index_data["stages"]:
            index_data["stages"][stage] = []
        index_data["stages"][stage].append(day)
        
        lessons_added += 1
        print(f"✓ 已生成第{day}天内容：{day_data['topic']}")
    
    # 更新索引统计
    index_data["total_lessons"] = len(index_data["lessons"])
    index_data["current_day"] = max([l["day"] for l in index_data["lessons"]], default=0)
    index_data["last_updated"] = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    # 保存索引
    save_index(index_data)
    
    print(f"\n初始化完成！")
    print(f"新增课程：{lessons_added} 天")
    print(f"总课程数：{index_data['total_lessons']} 天")
    print(f"当前进度：第{index_data['current_day']} 天")
    print(f"\n文件位置:")
    print(f"  学习内容：{DAILY_LESSONS_DIR}/")
    print(f"  练习题目：{PRACTICE_QUESTIONS_DIR}/")
    print(f"  索引导航：{INDEX_FILE}")


if __name__ == "__main__":
    main()
